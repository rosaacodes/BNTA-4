package JavaII.ConditionalAndIfStatements;

public class AllStatements {
    public static void main(String[] args){
        if (10 < 5) {
            // block of code to be executed if condition1 is true
            System.out.print("I love the if statement");

        } else if (10 < 6) {
            // block of code to be executed if above condition is false
            System.out.print("I love the else if one statement");

        } else if (10 > 7) {
            // block of code to be executed if above all conditions are false
            System.out.print("I love the else if two statement");

        } else {
            // block of code to be executed if above all conditions are false
            System.out.print("I love the else statement");

        }



    }
}
