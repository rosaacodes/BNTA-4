package JavaI.LoopingThroughArrays;

import java.util.Arrays;

public class Loopingthruarrays {

    public static void main(String[] args){

        String[] names = {"Jamila", "Mark"};

        int i = 0;
        while (i < names.length) {
            System.out.println(names[i]);
            i++;

        }

    }




    }
